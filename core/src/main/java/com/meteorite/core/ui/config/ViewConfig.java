package com.meteorite.core.ui.config;

/**
 * @author wei_jc
 * @version 1.0.0
 */
public class ViewConfig {
    /** 视图ID*/
    private int id;
    /** 视图名称 */
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
